# if
# Условие True всегда верно, поэтому выполнится команда print и выведет 'Hello, World!'
if True:
    print('Hello, World!')

# Условие False всегда неверно, поэтому этот блок не выполнится, и ничего не будет выведено
if False:
    print('Hello, World!')

# Пример использования if с условием. Переменная x имеет значение 10,
x = 10
if x > 0:
    print('x > 0')


# Oператоры if-elif-else позволяют проверить несколько условий
x = 0
if x > 11: # Если x больше 11, то выполняется блок if. Проверяем первое условие: x > 11.
    print('x is negative')
elif x > 1: # Иначе если x больше 1, то выполняется блок elif. Проверяем второе условие: x > 1.
    print('x is positive')
else: # Если все предыдущие условия ложны, выполняется блок else.
    print('x is zero')

"""     
Пояснение:
   if – проверяет истинность условия, и если оно истинно, выполняет блок кода.
   elif (else if) – позволяет проверять дополнительное условие, если предыдущее оказалось ложным.
   else – выполняется, если ни одно из предыдущих условий не было истинным.
"""

# Примеры использования if с несколькими условиями

x = 10
y = 20

# Вложенные операторы if
# Сначала проверяется первое условие: x > 0. Если оно истинно (а x действительно больше 0),
# выполняется следующий if, который проверяет y > 0. Если оба условия истинны, выводится 'Go!'
if x > 0:
    if y > 0:
        print('Go!')  # Выведет 'Go!', так как оба условия истинны

# Использование логического оператора "and"
# Оператор and позволяет объединить два условия в одной строке.
# Здесь проверяется, что и x > 0, и y > 0, и если оба условия истинны, выводится 'Go!'
if x > 0 and y > 0:
    print('Go!')  # Выведет 'Go!', так как оба условия истинны

# Этот блок аналогичен предыдущему, просто код немного сдвинут вправо (ошибки нет).
if x > 0 and y > 0:
        print('Go!')  # Также выведет 'Go!', так как условия те же

"""
Пояснение:
   Вложенные if: Можно писать один if внутри другого. Если первое условие истинно, тогда проверяется второе.
   Логический оператор and: Объединяет несколько условий. Если оба условия истинны, блок кода выполнится.
   Третий блок кода — аналог второго. Сдвиг вправо в этом случае не влияет на выполнение программы.
"""

message = 'Hello, World!'  # Переменная message содержит строку 'Hello, World!'
message2 = ''  # Переменная message2 — пустая строка

# Проверяем, содержит ли переменная message какую-то строку (не пустая ли она)
if message:
    print('message is not empty')  # Так как message содержит строку, выводится это сообщение

# Эквивалентное выражение через явное преобразование в булево значение
if bool(message):
    print('bool(message is not empty)')  # bool(message) вернет True, так как строка не пуста

# Проверка, что переменная message2 пуста (not проверяет на ложность)
if not message2:
    print('message2 is not empty')  # message2 пустая строка, поэтому условие not message2 истинно

# Эквивалентная проверка через bool для пустой строки
if not bool(message2):
    print('bool(message2 is not empty)')  # bool(message2) возвращает False, так как строка пуста

x = 10  # Переменная x содержит число 10

# Булева проверка числа. Любое ненулевое число в Python возвращает True
if bool(x):
    print('x is not empty')  # Число 10 не пусто (не равно 0), поэтому выводится это сообщение
"""
Пояснение:
    Проверка строки: Непустая строка в Python считается True. Поэтому if message: и if bool(message): работают одинаково.
    Пустая строка: Пустая строка считается False. Проверка if not message2: и if not bool(message2): также эквивалентны.
    Числовые значения: Ненулевые числа всегда возвращают True, а 0 — False.
"""

